# for variabel in range function
# kalau perulangannya diketahui bisa pake for 
# kalau hanya tau batasnya can ga tau pake while
for i in range(2):
    print('Welcome to everyone!!')

start = 2                       # output nya adalah 2,3,6,8 
stop = 10                       # angka 10 tidak dalam output karea      
step = 2                        # angka 10 merupakan batas stop
for j in range(start, stop, step):
    print(j)